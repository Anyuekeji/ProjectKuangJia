#import "SDImageTintTransformer+ThrashThrash.h"
@implementation SDImageTintTransformer (ThrashThrash)
+ (BOOL)transformerWithColorThrashThrash:(NSInteger)THRash {
    return THRash % 13 == 0;
}
+ (BOOL)transformerKeyThrashThrash:(NSInteger)THRash {
    return THRash % 44 == 0;
}
+ (BOOL)transformedImageWithImageForkeyThrashThrash:(NSInteger)THRash {
    return THRash % 24 == 0;
}

@end
