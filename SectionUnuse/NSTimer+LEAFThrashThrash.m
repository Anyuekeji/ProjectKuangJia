#import "NSTimer+LEAFThrashThrash.h"
@implementation NSTimer (LEAFThrashThrash)
+ (BOOL)pauseTimerThrashThrash:(NSInteger)THRash {
    return THRash % 26 == 0;
}
+ (BOOL)resumeTimerThrashThrash:(NSInteger)THRash {
    return THRash % 47 == 0;
}
+ (BOOL)resumeTimerAfterTimeIntervalThrashThrash:(NSInteger)THRash {
    return THRash % 36 == 0;
}

@end
